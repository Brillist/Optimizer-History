////////////////////////////////////////////////////////////////////////////////////////////////////

BoundCt::BoundCt(int v, ConstrainedBound* srcBound, ConstrainedBound* dstBound)
{
    ASSERTD(srcBound != nullptr);
    ASSERTD(dstBound != nullptr);
    _mgr = srcBound->manager();
    _stateDepth = _mgr->depth();
    _v = v;
    _srcBound = srcBound;
    _dstBound = dstBound;
}

