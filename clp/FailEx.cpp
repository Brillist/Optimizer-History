#include "libclp.h"
#include "FailEx.h"

////////////////////////////////////////////////////////////////////////////////////////////////////

UTL_NS_USE;
LUT_NS_USE;

////////////////////////////////////////////////////////////////////////////////////////////////////

UTL_CLASS_IMPL(clp::FailEx);

////////////////////////////////////////////////////////////////////////////////////////////////////

CLP_NS_BEGIN;

////////////////////////////////////////////////////////////////////////////////////////////////////

FailEx::FailEx(const String& str, uint_t label)
    : utl::Exception("inconsistency", nullptr)
{
    init(str.clone(), label);
}

////////////////////////////////////////////////////////////////////////////////////////////////////

void
FailEx::copy(const utl::Object& rhs)
{
    auto& ex = utl::cast<FailEx>(rhs);
    super::copy(ex);
    delete _str;
    _str = utl::clone(ex._str);
    _label = ex._label;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

void
FailEx::init(const String* str, const uint_t label)
{
    _str = str;
    _label = label;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

void
FailEx::deInit()
{
    delete _str;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

CLP_NS_END;
