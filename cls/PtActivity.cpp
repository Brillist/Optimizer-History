////////////////////////////////////////////////////////////////////////////////////////////////////

void
PtActivity::excludePt(uint_t pt)
{
    _possiblePts->remove(pt);
    if (_possiblePts->isBound())
    {
        selectPt(_possiblePts->value());
    }
}

////////////////////////////////////////////////////////////////////////////////////////////////////

void
PtActivity::init()
{
    _frozenPt = uint_t_max;
    _possiblePts = nullptr;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

void
PtActivity::deInit()
{
    delete _possiblePts;
}

