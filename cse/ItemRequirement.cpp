#include "libcse.h"
#include "ItemRequirement.h"

////////////////////////////////////////////////////////////////////////////////////////////////////

UTL_NS_USE;
LUT_NS_USE;
CLS_NS_USE;

////////////////////////////////////////////////////////////////////////////////////////////////////

UTL_CLASS_IMPL(cse::ItemRequirement, utl::Object);

////////////////////////////////////////////////////////////////////////////////////////////////////

CSE_NS_BEGIN;

////////////////////////////////////////////////////////////////////////////////////////////////////

void
ItemRequirement::copy(const Object& rhs)
{
    ASSERTD(rhs.isA(ItemRequirement));
    const ItemRequirement& rr = (const ItemRequirement&)rhs;
    _itemId = rr._itemId;
    _quantity = rr._quantity;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

void
ItemRequirement::serialize(Stream& stream, uint_t io, uint_t)
{
    utl::serialize(_itemId, stream, io);
    utl::serialize(_quantity, stream, io);
}

////////////////////////////////////////////////////////////////////////////////////////////////////

void
ItemRequirement::init()
{
    _itemId = uint_t_max;
    _quantity = uint_t_max;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

void
ItemRequirement::deInit()
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////

CSE_NS_END;
