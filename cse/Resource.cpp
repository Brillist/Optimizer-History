#include "libcse.h"
#include "Resource.h"

////////////////////////////////////////////////////////////////////////////////////////////////////

UTL_NS_USE;
LUT_NS_USE;
CLS_NS_USE;

////////////////////////////////////////////////////////////////////////////////////////////////////

UTL_CLASS_IMPL(cse::Resource, utl::Object);

////////////////////////////////////////////////////////////////////////////////////////////////////

CSE_NS_BEGIN;

////////////////////////////////////////////////////////////////////////////////////////////////////

void
Resource::copy(const Object& rhs)
{
    ASSERTD(rhs.isA(Resource));
    const Resource& res = (const Resource&)rhs;
    _id = res._id;
    _serialId = res._serialId;
    _name = res._name;
    _clsResource = res._clsResource;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

void
Resource::serialize(Stream& stream, uint_t io, uint_t)
{
    utl::serialize(_id, stream, io);
    lut::serialize(_name, stream, io);
}

////////////////////////////////////////////////////////////////////////////////////////////////////

void
Resource::init()
{
    _id = _serialId = utl::uint_t_max;
    _clsResource = nullptr;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

void
Resource::deInit()
{
}

////////////////////////////////////////////////////////////////////////////////////////////////////

CSE_NS_END;
