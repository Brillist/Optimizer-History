void
ScheduleEvaluatorConfiguration::copy(const Object& rhs)
{
    ASSERTD(rhs.isA(ScheduleEvaluatorConfiguration));
    const ScheduleEvaluatorConfiguration& cf = (const ScheduleEvaluatorConfiguration&)rhs;
    delete _schedulerConfig;
    _schedulerConfig = utl::clone(cf._schedulerConfig);
}

