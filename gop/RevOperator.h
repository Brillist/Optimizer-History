GOP_NS_BEGIN;

////////////////////////////////////////////////////////////////////////////////////////////////////

/**
   Reversible Operator (abstract).

   When an Operator executes it makes a change to the instructions for building an individual (Ind).
   A reversible Operator remembers the change it made, and upon request it can reverse what it did.

   \ingroup gop
*/

////////////////////////////////////////////////////////////////////////////////////////////////////

class RevOperator : public Operator
{
    UTL_CLASS_DECL_ABC(RevOperator, Operator);
    UTL_CLASS_DEFID;

public:
    /** Accept the last change. */
    virtual void
    accept()
    {
    }

    /** Undo the last change. */
    virtual void undo() = 0;
};

////////////////////////////////////////////////////////////////////////////////////////////////////

GOP_NS_END;
