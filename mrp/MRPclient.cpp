#include "libmrp.h"
#include "MRPclient.h"

/////////////////////////////////////////////////////////////////////////////////////////////////////

UTL_NS_USE;
CSE_NS_USE;

/////////////////////////////////////////////////////////////////////////////////////////////////////

UTL_CLASS_IMPL(mrp::MRPclient, cse::SEclient);

/////////////////////////////////////////////////////////////////////////////////////////////////////

MRP_NS_BEGIN;

/////////////////////////////////////////////////////////////////////////////////////////////////////

MRPclient::MRPclient(MRPserver* server,
                     utl::FDstream* socket,
                     const utl::InetHostAddress& addr,
                     bool recording)
    : SEclient((cse::Server*)server, socket, addr, recording)
{
    init();
}

/////////////////////////////////////////////////////////////////////////////////////////////////////

void
MRPclient::init()
{
    _mrpRun = new MRPrun();
}

/////////////////////////////////////////////////////////////////////////////////////////////////////

void
MRPclient::deInit()
{
    delete _mrpRun;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////

MRP_NS_END;
